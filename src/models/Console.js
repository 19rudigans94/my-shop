import mongoose from "mongoose";

const consoleSchema = new mongoose.Schema({
  title: {
    type: String,
    required: true,
  },
  state: {
    type: Boolean,
    required: true,
  },
  price: {
    type: Number,
    required: true,
  },
  stock: {
    type: Number,
    required: true,
  },
  youtubeUrl: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  image: {
    type: String,
    required: true,
  },
  slug: {
    type: String,
    required: true,
    unique: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
  updatedAt: {
    type: Date,
    default: Date.now,
  },
});

const Console =
  mongoose.models.Console || mongoose.model("Console", consoleSchema);

export default Console;
